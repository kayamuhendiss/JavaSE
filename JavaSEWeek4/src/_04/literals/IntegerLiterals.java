package _04.literals;

public class IntegerLiterals {
    public static void main(String[] args) {
        //decimal 10luk taban
        int number=100;  //100->integer litarali varsayilan int tipinde

        //octal
        int one=01;
        int two=02;
        int seven=07;
        int eight=010;
        int nine=011;
        //int compileError=08;
        System.out.println(eight);
        System.out.println(nine);

        //hexadecimal
        int hex=0x11;
        System.out.println(hex);
        int hex2=0xA;
        System.out.println(hex2);

        //binary
        int binary=0b11;
        System.out.println(binary);

    }


}
